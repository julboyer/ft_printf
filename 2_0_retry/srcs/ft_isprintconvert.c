int	ft_isprintconvert(char c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i' || c == 'u'
		|| c == 'x' || c == 'X' || c == '%' || c == 'n' || c == 'o' || c == 'f'
		|| c == 'e' || c == 'E' || c == 'g' || c == 'G')
		return (1);
	return (0);
}
