int		ft_isalnum(int c)
{
	if ((c >= 'a' && c <= 'z')
	|| (c >= 'A' && c <= 'Z')
	|| (c >= '0' && c <= '9'))
		return ((unsigned char)!0);
	else
		return ((unsigned char)0);
}
